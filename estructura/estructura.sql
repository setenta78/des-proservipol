CREATE PROCEDURE `Anular_Ferper`(
	IN `$folioPermiso` VARCHAR(12),
	IN `$codigoFuncionario` CHAR(7),
	IN `$tipoPermiso` INT,
	IN `$fechaInicio` DATE,
	IN `$fechaTermino` DATE,
	IN `$fechaRegistro` DATE,
	IN `$ip` VARCHAR(12),
	IN `$usuarioProservipol` CHAR(7),
	IN `$estadoPermiso` INT
)
BEGIN
	
   DECLARE EXIT HANDLER FOR SQLEXCEPTION
   
	BEGIN
		SELECT 'ERROR' message;
   	ROLLBACK;
   END;
   
   START TRANSACTION;
   	
		UPDATE FERPER 
		SET ESTADO_PERMISO = $estadoPermiso+1
			,FECHA_MODIFICA = $fechaRegistro
			,DIR_IP_MODIFICA = $ip
			,FUN_CODIGO_MODIFICA = $usuarioProservipol
		WHERE FOLIO_PERMISO = $folioPermiso COLLATE LATIN1_SPANISH_CI
		AND ESTADO_PERMISO = 1;
		
		DELETE F
		FROM SERVICIO S
		JOIN FUNCIONARIO_SERVICIO F ON F.UNI_CODIGO = S.UNI_CODIGO AND F.CORRELATIVO_SERVICIO = S.CORRELATIVO_SERVICIO
		WHERE F.FUN_CODIGO = $codigoFuncionario
		AND S.TSERV_CODIGO = $tipoPermiso
		AND S.FECHA BETWEEN $fechaInicio AND $fechaTermino;
		
		DELETE D
		FROM SERVICIO D
		JOIN (SELECT S.CORRELATIVO_SERVICIO, IF(CF.UNI_AGREGADO IS NULL, CF.UNI_CODIGO, CF.UNI_AGREGADO) COD_UNIDAD
				FROM CARGO_FUNCIONARIO CF
				JOIN MARCELO_FECHA MF ON MF.FECHA BETWEEN CF.FECHA_DESDE AND IF(CF.FECHA_HASTA IS NULL, $fechaTermino, DATE_ADD(CF.FECHA_HASTA, INTERVAL -1 DAY))
				JOIN SERVICIO S ON S.UNI_CODIGO = IF(CF.UNI_AGREGADO IS NULL, CF.UNI_CODIGO, CF.UNI_AGREGADO) AND S.FECHA = MF.FECHA
				WHERE MF.FECHA BETWEEN $fechaInicio AND $fechaTermino
				AND CF.FUN_CODIGO = $codigoFuncionario) A ON A.COD_UNIDAD = D.UNI_CODIGO AND A.CORRELATIVO_SERVICIO = D.CORRELATIVO_SERVICIO
		LEFT JOIN FUNCIONARIO_SERVICIO F ON F.UNI_CODIGO = D.UNI_CODIGO AND F.CORRELATIVO_SERVICIO = D.CORRELATIVO_SERVICIO
		WHERE D.TSERV_CODIGO = $tipoPermiso AND F.FUN_CODIGO IS NULL;
		
		SELECT 'OK' message;
	COMMIT WORK;
	
END//
