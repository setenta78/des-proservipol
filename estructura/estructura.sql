DELIMITER //
CREATE PROCEDURE `Informes_ListaServiciosDipladecar`(
	IN `@anno` INT,
	IN `@mes` INT
)
    READS SQL DATA
    COMMENT 'Extrae lista de servicios con el formato solicitado por DIpladec'
BEGIN
	SELECT 
		V.ZONA_DESCRIPCION ZONA,
		V.PREFECTURA_DESCRIPCION PREFECTURA,
		V.COMISARIA_DESCRIPCION COMISARIA,
		V.UNI_DESCRIPCION DESTACAMENTO,
		IF(TS.TSERV_CODIGO IN (1100,1200,1300), CONCAT(TS.TSERV_DESCRIPCION,' - ',TE.TEXT_DESCRIPCION), TS.TSERV_DESCRIPCION) 'NOMBRE SERVICIO',
		S.FECHA,
		S.HORA_INICIO 'HORA INICIO',
		S.HORA_TERMINO 'HORA TERMINO',
		TIMESTAMPDIFF(MINUTE, CONCAT(S.FECHA,' ',S.HORA_INICIO), CONCAT(IF(S.HORA_INICIO>=S.HORA_TERMINO,DATE_ADD(S.FECHA, INTERVAL 1 DAY),S.FECHA),' ',S.HORA_TERMINO)) 'DURACION SERVICIO (MINUTOS)',
		FS.FUN_CODIGO 'CODIGO FUNCIONARIO',
		F.FUN_RUT 'RUT FUNCIONARIO',
		F.FUN_APELLIDOPATERNO 'PRIMER APELLIDO',
		F.FUN_APELLIDOMATERNO 'SEGUNDO APELLIDO',
		F.FUN_NOMBRE 'NOMBRE',
		F.FUN_NOMBRE2 'SEGUNDO NOMBRE',
		G.GRA_DESCRIPCION 'GRADO FUNCIONARIO',
		VE.VEH_PATENTE 'PATENTE VEHICULO',
		MV.MVEH_DESCRIPCION 'MARCA VEHICULO',
		MOV.MODVEH_DESCRIPCION 'MODELO VEHICULO',
		TV.TVEH_DESCRIPCION 'TIPO VEHICULO',
		TB.TANIM_DESCRIPCION 'TIPO ANIMAL',
		B.CAB_NOMBRE 'NOMBRE ANIMAL',
		B.CAB_BCU 'CODIGO ANIMAL'
	FROM SERVICIO S
	JOIN VISTA_ARBOL_UNIDADES_NACIONAL_CON_PREFECTURAS V ON S.UNI_CODIGO = V.UNI_CODIGO
	JOIN FUNCIONARIO_SERVICIO FS ON S.UNI_CODIGO = FS.UNI_CODIGO AND S.CORRELATIVO_SERVICIO = FS.CORRELATIVO_SERVICIO
	JOIN TIPO_SERVICIO TS ON S.TSERV_CODIGO = TS.TSERV_CODIGO
	LEFT JOIN TIPO_EXTRAORDINARIO TE ON TE.TEXT_CODIGO = S.TEXT_CODIGO
	JOIN FUNCIONARIO F ON FS.FUN_CODIGO = F.FUN_CODIGO
	JOIN GRADO G ON F.ESC_CODIGO = G.ESC_CODIGO AND F.GRA_CODIGO = G.GRA_CODIGO
	LEFT JOIN FUNCIONARIO_VEHICULO FV ON FS.UNI_CODIGO = FV.FUN_UNI_CODIGO AND FS.CORRELATIVO_SERVICIO = FV.FUN_CORRELATIVO_SERVICIO AND FS.FUN_CODIGO = FV.FUN_CODIGO
	LEFT JOIN VEHICULO_SERVICIO VS ON FV.VEH_UNI_CODIGO = VS.UNI_CODIGO AND FV.VEH_CORRELATIVO_SERVICIO = VS.CORRELATIVO_SERVICIO AND FV.VEH_CODIGO = VS.VEH_CODIGO
	LEFT JOIN VEHICULO VE ON VS.VEH_CODIGO = VE.VEH_CODIGO
	LEFT JOIN MODELO_VEHICULO MOV ON VE.MODVEH_CODIGO = MOV.MODVEH_CODIGO AND VE.MVEH_CODIGO = MOV.MVEH_CODIGO
	LEFT JOIN MARCA_VEHICULO MV ON MOV.MVEH_CODIGO = MV.MVEH_CODIGO
	LEFT JOIN TIPO_VEHICULO TV ON VE.TVEH_CODIGO = TV.TVEH_CODIGO
	LEFT JOIN ARMA_SERVICIO SA ON S.UNI_CODIGO = SA.UNI_CODIGO AND S.CORRELATIVO_SERVICIO = SA.CORRELATIVO_SERVICIO AND SA.FUN_CODIGO = F.FUN_CODIGO
	LEFT JOIN ARMA A ON SA.ARM_CODIGO = A.ARM_CODIGO
	LEFT JOIN TIPO_ARMA TA ON A.TARM_CODIGO = TA.TARM_CODIGO
	LEFT JOIN FUNCIONARIO_ANIMAL FB ON FS.UNI_CODIGO = FB.FUN_UNI_CODIGO AND FS.CORRELATIVO_SERVICIO = FB.FUN_CORRELATIVO_SERVICIO AND FS.FUN_CODIGO = FB.FUN_CODIGO
	LEFT JOIN ANIMALES_SERVICIO BS ON FB.ANIM_UNI_CODIGO = BS.UNI_CODIGO AND FB.ANIM_CORRELATIVO_SERVICIO = BS.CORRELATIVO_SERVICIO AND FB.ANIM_CODIGO = BS.ANIM_CODIGO
	LEFT JOIN CABALLO B ON BS.ANIM_CODIGO = B.CAB_CODIGO
	LEFT JOIN TIPO_ANIMAL TB ON B.TANI_CODIGO = TB.TANIM_CODIGO
	WHERE YEAR(S.FECHA) = @anno AND MONTH(S.FECHA) = @mes
	AND V.TUNI_CODIGO IN (1,11,111,2);
END//
