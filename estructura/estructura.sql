	IN `P_codigoFuncionario` VARCHAR(7),
	IN `P_FechaInicio` VARCHAR(8),
	IN `P_FechaTermino` VARCHAR(8),
	IN `P_TipoServicio` INT
)
    COMMENT 'Transaccion para eliminar servicios de forma masiva'
BEGIN

   DECLARE EXIT HANDLER FOR SQLEXCEPTION
   
	BEGIN
		SELECT 'ERROR' message;
   	ROLLBACK;
   END;
   
   START TRANSACTION;
	
		DELETE FUNCIONARIO_SERVICIO
		FROM SERVICIO 
		JOIN FUNCIONARIO_SERVICIO ON (FUNCIONARIO_SERVICIO.UNI_CODIGO = SERVICIO.UNI_CODIGO) AND (FUNCIONARIO_SERVICIO.CORRELATIVO_SERVICIO = SERVICIO.CORRELATIVO_SERVICIO)
		WHERE FUNCIONARIO_SERVICIO.FUN_CODIGO = P_codigoFuncionario
		AND SERVICIO.FECHA BETWEEN P_FechaInicio AND P_FechaTermino
		AND SERVICIO.TSERV_CODIGO = P_TipoServicio;
		
		DELETE D
		FROM SERVICIO D
		JOIN (SELECT S.CORRELATIVO_SERVICIO, IF(CF.UNI_AGREGADO IS NULL, CF.UNI_CODIGO, CF.UNI_AGREGADO) COD_UNIDAD
				FROM CARGO_FUNCIONARIO CF
				JOIN MARCELO_FECHA MF ON MF.FECHA BETWEEN CF.FECHA_DESDE AND IF(CF.FECHA_HASTA IS NULL, P_FechaTermino, DATE_ADD(CF.FECHA_HASTA, INTERVAL -1 DAY))
				JOIN SERVICIO S ON S.UNI_CODIGO = IF(CF.UNI_AGREGADO IS NULL, CF.UNI_CODIGO, CF.UNI_AGREGADO) AND S.FECHA = MF.FECHA
				WHERE MF.FECHA BETWEEN P_FechaInicio AND P_FechaTermino
				AND CF.FUN_CODIGO = P_codigoFuncionario) A ON A.COD_UNIDAD = D.UNI_CODIGO AND A.CORRELATIVO_SERVICIO = D.CORRELATIVO_SERVICIO
		LEFT JOIN FUNCIONARIO_SERVICIO F ON F.UNI_CODIGO = D.UNI_CODIGO AND F.CORRELATIVO_SERVICIO = D.CORRELATIVO_SERVICIO
		WHERE D.TSERV_CODIGO = P_TipoServicio AND F.FUN_CODIGO IS NULL;
	
		SELECT 'OK' message;
	COMMIT WORK;
	
