CREATE PROCEDURE `BorraServiciosEspecificos`(
	IN `$codUnidad` INT,
	IN `$correlativo` INT,
	IN `$codFuncionario` VARCHAR(7)
)
BEGIN
	
	DELETE F
	FROM FUNCIONARIO_VEHICULO F
	WHERE F.FUN_UNI_CODIGO = $codUnidad
	AND F.FUN_CORRELATIVO_SERVICIO = $correlativo
	AND F.FUN_CODIGO = $codFuncionario;
		
	IF (SELECT COUNT(1) FROM VEHICULO_SERVICIO S WHERE S.UNI_CODIGO = $codUnidad AND S.CORRELATIVO_SERVICIO = $correlativo) = 0 THEN
		DELETE S
		FROM VEHICULO_SERVICIO S
		WHERE S.UNI_CODIGO = $codUnidad
		AND S.CORRELATIVO_SERVICIO = $correlativo;	
	END IF;
	
	DELETE F
	FROM CUADRANTE_SERVICIO F
	WHERE F.UNI_CODIGO = $codUnidad
	AND F.CORRELATIVO_SERVICIO = $correlativo
	AND F.FUN_CODIGO = $codFuncionario;
	
	DELETE F
	FROM ARMA_SERVICIO F
	WHERE F.UNI_CODIGO = $codUnidad
	AND F.CORRELATIVO_SERVICIO = $correlativo
	AND F.FUN_CODIGO = $codFuncionario;
	
	DELETE F
	FROM ACCESORIO_SERVICIO F
	WHERE F.UNI_CODIGO = $codUnidad
	AND F.CORRELATIVO_SERVICIO = $correlativo
	AND F.FUN_CODIGO = $codFuncionario;
	
	DELETE F
	FROM FUNCIONARIO_VIDEOCAMARA F
	WHERE F.UNI_CODIGO = $codUnidad
	AND F.CORRELATIVO_SERVICIO = $correlativo
	AND F.FUN_CODIGO = $codFuncionario;
	
	DELETE F
	FROM FUNCIONARIO_SERVICIO F
	WHERE F.UNI_CODIGO = $codUnidad
	AND F.CORRELATIVO_SERVICIO = $correlativo
	AND F.FUN_CODIGO = $codFuncionario;
	
	IF (SELECT COUNT(1) FROM FUNCIONARIO_SERVICIO F	WHERE F.UNI_CODIGO = $codUnidad AND F.CORRELATIVO_SERVICIO = $correlativo AND F.FUN_CODIGO = $codFuncionario) = 0 THEN
		DELETE F
		FROM SERVICIO F
		WHERE F.UNI_CODIGO = $codUnidad
		AND F.CORRELATIVO_SERVICIO = $correlativo;
	END IF;

