CREATE PROCEDURE `eliminarTarjetaCombustible`(
	IN `$CodVehiculo` INT,
	IN `$NroTarjeta` VARCHAR(8),
	IN `$NroTarjetaDV` VARCHAR(1)
)
BEGIN
DECLARE EXIT HANDLER FOR SQLEXCEPTION
   
	BEGIN
		SELECT 'ERROR' message;
   	ROLLBACK;
   END;
   
   START TRANSACTION;
   
   	SET @correlativo = (SELECT IFNULL(MAX(S.TC_CODIGO),1) R FROM TARJETA_COMBUSTIBLE S WHERE S.COD_VEH = $CodVehiculo AND S.TC_NRO_TARJETA = $NroTarjeta AND S.TC_NRO_TARJETA_DV = $NroTarjetaDV AND S.TC_FECHA_HASTA IS NULL);
   	
	   DELETE FROM TARJETA_COMBUSTIBLE WHERE COD_VEH = $CodVehiculo AND TC_NRO_TARJETA = $NroTarjeta AND TC_NRO_TARJETA_DV = $NroTarjetaDV AND TC_FECHA_HASTA IS NULL;
		
		UPDATE TARJETA_COMBUSTIBLE SET TC_FECHA_HASTA = NULL WHERE COD_VEH = $CodVehiculo AND TC_CODIGO = @correlativo-1;
		
		SELECT 'OK' message;
		
	COMMIT WORK;
