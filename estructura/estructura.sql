-- Volcando estructura para procedimiento DB_PROSERVIPOL_V3.ControlTarjetasCombustible
DELIMITER //
CREATE PROCEDURE `ControlTarjetasCombustible`()
BEGIN

    -- Crear tabla temporal
    CREATE TEMPORARY TABLE tmp_ultimos_servicios (
        VEH_CODIGO INT,
        TC_NRO_TARJETA VARCHAR(8),
		  TC_NRO_TARJETA_DV VARCHAR(1),
        FechaCarga DATE,
        Menos7 DATE,
        Menos10 DATE,
        Km500 DATE,
        Km800 DATE,
        COD_COLOR INT,
        PRIMARY KEY (VEH_CODIGO)
    );
    
    -- Insertar fechas de los 7 y 10 ultimos servicios, y de los servicios que cumplieron 500 y 800 km
    INSERT INTO tmp_ultimos_servicios (VEH_CODIGO, Menos7, Menos10, Km500, Km800)
	   SELECT
		    VEH_CODIGO,
			 CAST(COALESCE(MAX(IF(NUM = 7, FECHA, NULL)), NULL) AS DATE) AS fecha7,
			 CAST(COALESCE(MAX(IF(NUM = 10, FECHA, NULL)), NULL) AS DATE) AS fecha10,
			 CAST(COALESCE(MAX(IF(KM_ANT < 500 AND KM_ACUM >= 500, FECHA, NULL)), NULL) AS DATE) AS km500,
			 CAST(COALESCE(MAX(IF(KM_ANT < 800 AND KM_ACUM >= 800, FECHA, NULL)), NULL) AS DATE) AS km800
		FROM (SELECT
		        t.VEH_CODIGO,
		        t.FECHA,
		        @num := IF(@veh = t.VEH_CODIGO, @num + 1, 1) AS NUM,
		        @km_ant AS KM_ANT,
		        @km := IF(@veh = t.VEH_CODIGO, @km + (t.KM_FINAL - t.KM_INICIAL), (t.KM_FINAL - t.KM_INICIAL)) AS KM_ACUM,
		        @km_ant := @km,
		        @veh := t.VEH_CODIGO
		    FROM (SELECT 
		            V.VEH_CODIGO,
		            S.FECHA,
		            VS.KM_INICIAL,
		            VS.KM_FINAL
		        FROM VEHICULO V
				  JOIN TIPO_VEHICULO TV ON TV.TVEH_CODIGO = V.TVEH_CODIGO AND TV.TVEH_TARJETA_COMBUSTIBLE = 1
				  JOIN ESTADO_VEHICULO E ON V.VEH_CODIGO = E.VEH_CODIGO AND E.FECHA_HASTA IS NULL AND E.EST_CODIGO NOT IN (3500)
				  LEFT JOIN VEHICULO_SERVICIO VS ON VS.VEH_CODIGO = V.VEH_CODIGO
				  LEFT JOIN SERVICIO S ON S.UNI_CODIGO = VS.UNI_CODIGO AND S.CORRELATIVO_SERVICIO = VS.CORRELATIVO_SERVICIO
		        GROUP BY V.VEH_CODIGO, S.FECHA
		        ORDER BY V.VEH_CODIGO, S.FECHA DESC
		    ) t,
		    (SELECT @num := 0, @km := 0, @km_ant := 0, @veh := NULL) vars
		) X
		GROUP BY VEH_CODIGO;
	
	-- Actualizar nro tarjeta, fecha carga combustible y color
	UPDATE tmp_ultimos_servicios T
	JOIN TARJETA_COMBUSTIBLE TC ON TC.COD_VEH = T.VEH_CODIGO AND TC.TC_FECHA_HASTA IS NULL
	SET T.TC_NRO_TARJETA = TC.TC_NRO_TARJETA,
   	 T.TC_NRO_TARJETA_DV = TC.TC_NRO_TARJETA_DV,
		 T.FechaCarga = TC.FECHA_CARGA_COMBUSTIBLE,
		 T.COD_COLOR = 
			CASE
				-- 10
				WHEN (TC.FECHA_CARGA_COMBUSTIBLE IS NULL) THEN 10
				-- 9
				WHEN (TC.FECHA_CARGA_COMBUSTIBLE < T.Menos10 AND TC.FECHA_CARGA_COMBUSTIBLE < T.Km800) THEN 9
				-- 8
				WHEN (TC.FECHA_CARGA_COMBUSTIBLE < T.Menos10 AND TC.FECHA_CARGA_COMBUSTIBLE < T.Km500) THEN 8
				-- 7
				WHEN (TC.FECHA_CARGA_COMBUSTIBLE < T.Menos10) THEN 7
				-- 6
				WHEN (TC.FECHA_CARGA_COMBUSTIBLE < T.Menos7 AND TC.FECHA_CARGA_COMBUSTIBLE < T.Km800) THEN 6
				-- 5
				WHEN (TC.FECHA_CARGA_COMBUSTIBLE < T.Km800) THEN 5
				-- 4
				WHEN (TC.FECHA_CARGA_COMBUSTIBLE < T.Menos7 AND TC.FECHA_CARGA_COMBUSTIBLE < T.Km500) THEN 4
				-- 3
				WHEN (TC.FECHA_CARGA_COMBUSTIBLE < T.Km500) THEN 3
				-- 2
				WHEN (TC.FECHA_CARGA_COMBUSTIBLE < T.Menos7) THEN 2
				ELSE 1
		END;
    
    -- Update a los vehiculos que presenten un nuevo registro
    UPDATE CARGA_COMBUSTIBLE C
    JOIN tmp_ultimos_servicios T ON T.VEH_CODIGO = C.VEH_CODIGO AND C.CC_ACTIVO = 1
    SET C.CC_ACTIVO = 0;
    
    -- Insertar Valores
	 INSERT INTO CARGA_COMBUSTIBLE (VEH_CODIGO, ECC_CODIGO, TC_NRO_TARJETA, TC_NRO_TARJETA_DV, CC_FECHA_REGISTRO, CC_FECHA_CARGA, CC_ACTIVO)
	 SELECT 
		 	VEH_CODIGO,
			COD_COLOR ECC_CODIGO,
			TC_NRO_TARJETA,
			TC_NRO_TARJETA_DV,
			DATE(NOW()) CC_FECHA_REGISTRO,
			FechaCarga,
			1 CC_ACTIVO
	 FROM tmp_ultimos_servicios;
    
    -- Borrar tabla temporal
	 DROP TEMPORARY TABLE IF EXISTS tmp_ultimos_servicios;
	COMMIT;

END//
