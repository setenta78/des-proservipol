CREATE PROCEDURE `procedimiento_guardar_servicios`(
	IN `P_codigoFuncionario` VARCHAR(7),
	IN `P_FechaInicio` VARCHAR(8),
	IN `P_FechaTermino` VARCHAR(8),
	IN `P_TipoServicio` INT
)
    COMMENT 'Transaccion para insertar servicios de forma masiva'
BEGIN

   DECLARE EXIT HANDLER FOR SQLEXCEPTION
   
	BEGIN
		SELECT 'ERROR' message;
   	ROLLBACK;
   END;
   
   START TRANSACTION;
   	
   	SET @dias = DATEDIFF(P_FechaTermino,P_FechaInicio);
	   SET @i = 0;
	   
   	WHILE @i<=@dias DO
	
			INSERT INTO SERVICIO (CORRELATIVO_SERVICIO, UNI_CODIGO,TSERV_CODIGO,FECHA,HORA_INICIO,HORA_TERMINO)
			SELECT 0, IF(CF.UNI_AGREGADO IS NULL, CF.UNI_CODIGO, CF.UNI_AGREGADO) COD_UNIDAD, P_TipoServicio, MF.FECHA, '00:00:00' horaInicio,'00:00:00' horaTermino
			FROM CARGO_FUNCIONARIO CF
			JOIN MARCELO_FECHA MF ON MF.FECHA BETWEEN CF.FECHA_DESDE AND IF(CF.FECHA_HASTA IS NULL, DATE_ADD(P_FechaInicio, INTERVAL @i DAY), DATE_ADD(CF.FECHA_HASTA, INTERVAL -1 DAY))
			WHERE MF.FECHA = DATE_ADD(P_FechaInicio, INTERVAL @i DAY)
			AND CF.FUN_CODIGO = P_codigoFuncionario;
			
			INSERT INTO FUNCIONARIO_SERVICIO (UNI_CODIGO, CORRELATIVO_SERVICIO, FUN_CODIGO, NUMERO_MEDIO, FACT_CODIGO)
			SELECT IF(CF.UNI_AGREGADO IS NULL, CF.UNI_CODIGO, CF.UNI_AGREGADO) COD_UNIDA,
				(SELECT MAX(SS.CORRELATIVO_SERVICIO) AS CORRELATIVO 
				FROM SERVICIO SS 
				WHERE SS.UNI_CODIGO = IF(CF.UNI_AGREGADO IS NULL, CF.UNI_CODIGO, CF.UNI_AGREGADO)
				AND SS.TSERV_CODIGO = P_TipoServicio AND SS.FECHA = DATE_ADD(P_FechaInicio, INTERVAL @i DAY)) CORRELATIVO,
				CF.FUN_CODIGO,1,NULL
			FROM CARGO_FUNCIONARIO CF
			JOIN MARCELO_FECHA MF ON MF.FECHA BETWEEN CF.FECHA_DESDE AND IF(CF.FECHA_HASTA IS NULL, DATE_ADD(P_FechaInicio, INTERVAL @i DAY), DATE_ADD(CF.FECHA_HASTA, INTERVAL -1 DAY))
			WHERE MF.FECHA = DATE_ADD(P_FechaInicio, INTERVAL @i DAY)
			AND CF.FUN_CODIGO = P_codigoFuncionario;
			
	      SET @i = @i+1;
	   
   	END WHILE;
		
		SELECT 'OK' message;
	COMMIT WORK;
	
